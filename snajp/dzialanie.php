	<h3 class="head3">Zasada działania snajpera</h3>
	<hr>
	
	<p>
		Celem aplikacji jest automatyczne składanie ofert w licytacjach serwisu allegro.
	</p>
	<p>		
		Największą zaletą snajpera jest możliwość ustawienie czasu automatycznej licytacji. Dzięki temu, składając ofertę kilka sekund przed końcem aukcji, zmniejszamy szanse na przelicytowanie naszej kwoty.
	</p>
	<p>
		Mechanizm odliczania czasu działa po stronie klienta (użytkownika), dlatego, do zakończenia działania skryptu, niezbędne jest zachowanie otwartej karty/okna przeglądarki, w kórej został uruchominy.
	</p>
	<p>
		Technologie wykorzystane do działania Snajpera:
		<ul>
			<li> HTML</li>
			<li> CSS</li>
			<li> Java Script</li>
			<li> PHP</li>
			<li> MySQL</li>
			<li> Bootstrap</li>
			<li> Serwer WAMP</li>
			<li> ApiAllegro</li>
		</ul>
	</p>
